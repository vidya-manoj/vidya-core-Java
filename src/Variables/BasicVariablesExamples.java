package Variables;

public class BasicVariablesExamples {

    public static void main(String[] args) {

        int age = 21;
        short population = -32768;  // -32768 to 3276
        byte count = -55;  // -127 to 128
        long distance = 123456789125789L;

        // Floating data types

        float pi = 3.14f;
        double temperature = 25.999;

        char grade = 'C';
        boolean isStudent = true;

        System.out.println("Grade:: "+ grade);


        }
}
